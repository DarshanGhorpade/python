# Quick quiz
# Q. Write a python program to print the content of a list using a while loop.

# Take a list
fruits = ['Banana', 'Watermelon', 'Mango', 'Apple', 'Pineapple', 'Grapes', 'Pomegranate', 'Papaya', 'Mudapple']

# Initialise i to 0
i = 0

# Using while loop
while i<len(fruits):
    print(fruits[i])
    i = i + 1