# range function in Python
# The range function is used to generate a sequence of numbers
# We can also specify the start, stop and step-size as follows:
# range(start, stop, step-size)

# This will print numbers start from 0 to 7
for i in range(8):
    print(i)

# This will print numbers start from 1 to 7
for i in range(1, 8):
    print(i)

# This will print numbers start from 1 to 7 by step of 2
for i in range(1, 8, 2):
    print(i)