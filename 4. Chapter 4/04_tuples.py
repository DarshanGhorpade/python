# Tuples in python
# Tuple is immutable datatype in python.
# Once defined a tuples elements can't be altered or manipulated

# Creating a tuple using parenthesis ()
t = (1, 2, 3, 4)

# Printing the element of tuple
print(t[0])

# Cannot update the values of a tuple
# t[0] = 0    # Will not work, throws an error

t1 =()  # Empty tuple
print(t1)

t1 = (1)    # wrong way to declare a tuple with single element
t1 = (1,)   # tuple with single element
print(t1)