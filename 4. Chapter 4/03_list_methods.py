# List Methods

# sort method: to sort in ascending
l1 = [1, 8, 7, 2, 21, 15]
print(l1)
l1.sort()
print(l1)

# reverse method: reverses the list
l1.reverse()
print(l1)

# append: appends at the end of the list
l1.append(45)
print(l1)

# insert: to insert the number at given position/index
l1.insert(0, 5)  # will insert 5 at index 0
print(l1)

# pop: removes the element at given index
l1.pop(2)  # removes element at index 2 
print(l1)

# remove: remove given element from list
l1.remove(21)   # removes 21 from the list
print(l1)