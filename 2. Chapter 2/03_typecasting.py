# type function and typecasting
# type function is used to find the datatype of given variable in python

# typecasting

a = "3534"
print(type(a)) # this will print object of class str

# now typecast a to int, as a contains string with integer
a = int(a)
print(type(a)) # this will print object of class int

# Now we can perform addition on a
print(a + 5)

# "31" is string literal and 31 is numeric literal