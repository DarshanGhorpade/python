# Escape sequences in Python

# Escape sequence character
# Sequence of characters after backslash \
# Escape sequence character comprises of more than one characters but represents one character when used within strings.

# \n : new line character
# \t : tab character
# \' : for using single quote
# \\ : for using \
story = "Darshan is good.\nHe\t is ve\\ry good."
print(story)