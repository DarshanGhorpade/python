# Conditional Expressions in Python

# If else and elif in Python
# If else and elif are a mutiway decision taken by our program due to certain conditions in our code.

a = 45

# 1. if-elif-else ladder in Python
if(a>3):
    print("The value of a is greater than 3")
elif(a>7):
    print("The value of a is greater than 7")
else:
    print("The value of a is not greater than 3 or 7")


# 2. Multiple if statements
if(a>3):
    print("The value of a is greater than 3")
if(a>7):
    print("The value of a is greater than 7")
if(a>13):
    print("The value of a is greater than 13")
if(a>17):
    print("The value of a is greater than 17")